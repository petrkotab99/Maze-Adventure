namespace Maze_Adventure
{
    /// <summary>
    /// Represent a movement direction.
    /// </summary>
    enum Direction
    {
        /// <summary>
        /// No direction,
        /// staying on place
        /// </summary>
        None,
        /// <summary>
        /// Up direction
        /// </summary>
        Up,
        /// <summary>
        /// Left direction
        /// </summary>
        Left,
        /// <summary>
        /// Down direction
        /// </summary>
        Down,
        /// <summary>
        /// Right direction
        /// </summary>
        Right,
    }
}
